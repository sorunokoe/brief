#[cfg(test)]
mod tests {
    use super::*;
    use crate::lexer::lex;
    use crate::parser::parse;

    fn doc_src(src: &str) -> String {
        let (tokens, _) = lex(src);
        let (program, _) = parse(&tokens, src);
        render(&program, "test")
    }

    #[test]
    fn renders_task_goal() {
        let md = doc_src(r#"task Hello : TaskBrief { goal = "Say hello" }"#);
        assert!(md.contains("Say hello"), "missing goal in:\n{md}");
    }

    #[test]
    fn renders_type_alias_table() {
        let md = doc_src(r#"type Email = @matches("[^@]+") String"#);
        assert!(md.contains("## Type Aliases"), "missing section:\n{md}");
        assert!(md.contains("Email"), "{md}");
        assert!(md.contains("@matches"), "{md}");
    }

    #[test]
    fn renders_effect_group() {
        let md = doc_src(r#"type AuthEffects = [Auth, Session]"#);
        assert!(md.contains("## Effect Groups"), "{md}");
        assert!(md.contains("AuthEffects"), "{md}");
        assert!(md.contains("Auth"), "{md}");
    }

    #[test]
    fn renders_sealed_type_variants() {
        let md = doc_src(r#"sealed type Status = Active | Inactive | Suspended(String)"#);
        assert!(md.contains("## Sealed Types"), "{md}");
        assert!(md.contains("Active"), "{md}");
        assert!(md.contains("Suspended"), "{md}");
    }

    #[test]
    fn renders_inline_effect_with_once() {
        let md = doc_src(r#"
            effect Payment {
                fn charge(amount: String) -> @once Handle
            }
        "#);
        assert!(md.contains("## Effects"), "{md}");
        assert!(md.contains("@once"), "{md}");
        assert!(md.contains("charge"), "{md}");
    }

    #[test]
    fn renders_perform_calls_in_step() {
        let md = doc_src(r#"
            import skill "GraphQL"
            task Fetch : TaskBrief uses [GraphQL] {
                goal = "fetch user"
                step Load {
                    let user = perform GraphQL.query(userQuery)?;
                }
            }
        "#);
        assert!(md.contains("GraphQL"), "{md}");
        assert!(md.contains("query"), "{md}");
        assert!(md.contains("user"), "{md}");
    }

    #[test]
    fn renders_toc_with_tasks() {
        let md = doc_src(r#"task Foo : TaskBrief { goal = "x" }"#);
        assert!(md.contains("## Contents"), "{md}");
        assert!(md.contains("Task: Foo"), "{md}");
    }

    #[test]
    fn renders_struct_with_constraints() {
        let md = doc_src(r#"
            struct User {
                email: @matches("[^@]+") String
                name: @nonEmpty String
            }
        "#);
        assert!(md.contains("## Structs"), "{md}");
        assert!(md.contains("email"), "{md}");
        assert!(md.contains("@matches"), "{md}");
        assert!(md.contains("@nonEmpty"), "{md}");
    }
}
